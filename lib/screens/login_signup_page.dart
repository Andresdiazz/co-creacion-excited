  void initiateFacebookLogin() async {
    var facebookLogin = FacebookLogin();
    var facebookLoginResult =
    await facebookLogin.logInWithReadPermissions(['email']);
    switch (facebookLoginResult.status) {
      case FacebookLoginStatus.error:
        print("Error");
        onLoginStatusChanged(false);
        break;
      case FacebookLoginStatus.cancelledByUser:
        print("CancelledByUser");
        onLoginStatusChanged(false);
        break;
      case FacebookLoginStatus.loggedIn:
        Navigator.pushNamed(context, '/second');
        print("LoggedIn");
        onLoginStatusChanged(true);
        break;
    }
  }




  Future<FirebaseUser> _handleSignIn(BuildContext context) async {
    final GoogleSignInAccount googleUser = await _googleSignIn.signIn();
    final GoogleSignInAuthentication googleAuth = await googleUser.authentication;

    final AuthCredential credential = GoogleAuthProvider.getCredential(
      accessToken: googleAuth.accessToken,
      idToken: googleAuth.idToken,
    );

    final FirebaseUser userDetails = await _auth.signInWithCredential(credential);
    ProviderDetails providerInfo = new ProviderDetails(userDetails.providerId);

    List<ProviderDetails> providerData = new List<ProviderDetails>();
    providerData.add(providerInfo);

    UserDetails details = new UserDetails(userDetails.providerId, userDetails.displayName,
        userDetails.photoUrl,
        userDetails.email,
        providerData);

    Navigator.push(context,
        MaterialPageRoute(builder: (context)=> new IntroPageView(detailsUser: details))
    );

    return userDetails;




  }

  Future<FirebaseUser> getUser() async {
    return await _auth.currentUser();
  }

  void signUpWithEmail() async {
    // marked async
    FirebaseUser user;
    try {
      user = await _auth.createUserWithEmailAndPassword(
        email: signupEmailController.text,
        password: signupPasswordController.text,
      );
    } catch (e) {
      print(e.toString());
    } finally {
      if (user != null) {
        Navigator.pushNamed(context, '/second');
        // sign in successful!
        // ex: bring the user to the home page
      } else {
        showInSnackBar("Usuario ya registrado");
        // sign in unsuccessful
        // ex: prompt the user to try again
      }
    }
  }

  void signInWithEmail() async {
    // marked async
    FirebaseUser user;
    try {
      user = await _auth.signInWithEmailAndPassword(
          email: loginEmailController.text, password: loginPasswordController.text);
    } catch (e) {
      print(e.toString());
    } finally {
      if (user != null) {
        Navigator.pushNamed(context, '/second');
